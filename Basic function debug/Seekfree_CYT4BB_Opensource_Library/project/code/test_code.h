// 使用示例     test_result_enum result = test_motor_control();
// 备注信息     测试电机控制功能
//-------------------------------------------------------------------------------------------------------------------
test_result_enum test_motor_control(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     平衡控制测试
// 返回参数     test_result_enum - 测试结果
// 使用示例     test_result_enum result = test_balance_control();
// 备注信息     测试平衡控制功能
//-------------------------------------------------------------------------------------------------------------------
