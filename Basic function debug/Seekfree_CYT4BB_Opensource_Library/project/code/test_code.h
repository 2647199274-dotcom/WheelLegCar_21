//-------------------------------------------------------------------------------------------------------------------
// 函数简介     显示测试结果
// 返回参数     void
// 使用示例     test_display_result();
// 备注信息     在显示屏上显示测试结果
//-------------------------------------------------------------------------------------------------------------------
