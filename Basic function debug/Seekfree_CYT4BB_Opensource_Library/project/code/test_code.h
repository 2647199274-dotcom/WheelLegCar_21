//-------------------------------------------------------------------------------------------------------------------
// 函数简介     VMC运动学测试
// 返回参数     test_result_enum - 测试结果
// 使用示例     test_result_enum result = test_vmc_control();
// 备注信息     测试VMC虚拟运动链控制
//-------------------------------------------------------------------------------------------------------------------
