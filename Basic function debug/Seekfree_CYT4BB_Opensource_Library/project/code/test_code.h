// 使用示例     test_run_all();
// 备注信息     依次执行所有测试项目
//-------------------------------------------------------------------------------------------------------------------
void test_run_all(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     IMU传感器测试
// 返回参数     test_result_enum - 测试结果
// 使用示例     test_result_enum result = test_imu_sensor();
// 备注信息     测试IMU传感器数据是否正常
//-------------------------------------------------------------------------------------------------------------------
test_result_enum test_imu_sensor(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     舵机控制测试
// 返回参数     test_result_enum - 测试结果
// 使用示例     test_result_enum result = test_steer_control();
// 备注信息     测试舵机控制功能
//-------------------------------------------------------------------------------------------------------------------
