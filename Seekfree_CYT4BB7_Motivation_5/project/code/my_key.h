// 备注信息     处理确认按键的逻辑
//-------------------------------------------------------------------------------------------------------------------
void key_enter(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     向下按键处理函数
// 返回参数     void
// 使用示例     key_down();
// 备注信息     处理向下按键的逻辑
//-------------------------------------------------------------------------------------------------------------------
void key_down(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     退出按键处理函数
// 返回参数     void
// 使用示例     key_quit();
// 备注信息     处理退出按键的逻辑
//-------------------------------------------------------------------------------------------------------------------
