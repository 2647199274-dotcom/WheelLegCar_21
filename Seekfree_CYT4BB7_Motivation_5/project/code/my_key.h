//===========================按键================================
//定义按键功能
typedef enum
{
    KEY_SELECT_UP,                                                                // 向上
    KEY_SELECT_DOWM,                                                              // 向下
    KEY_SELECT_QUIT,                                                              // 退出
    KEY_SELECT_ENTER,                                                             // 确认
    KEY_SELECT_MEMORY,                                                            // 写入内存
    KEY_SELECT_ADD,                                                             // 增加数字
    KEY_SELECT_SUB,                                                            // 减少
    KEY_SELECT_NULL
}key_select_enum;

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     按键初始化
// 参数说明     period - 按键扫描周期（毫秒）
