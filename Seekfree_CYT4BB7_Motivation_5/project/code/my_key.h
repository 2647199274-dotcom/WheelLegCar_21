//-------------------------------------------------------------------------------------------------------------------
// 函数简介     按键选择处理函数
// 返回参数     bool - 有按键按下返回true，否则返回false
// 使用示例     bool has_key = key_select();
// 备注信息     扫描按键状态并处理按键事件
//-------------------------------------------------------------------------------------------------------------------
bool key_select(void);

