// 使用示例     key_add();
// 备注信息     处理增加按键的逻辑
//-------------------------------------------------------------------------------------------------------------------
void key_add(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     减少按键处理函数
// 返回参数     void
// 使用示例     key_sub();
// 备注信息     处理减少按键的逻辑
//-------------------------------------------------------------------------------------------------------------------
