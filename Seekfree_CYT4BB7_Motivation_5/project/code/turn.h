#ifndef _TURN_H_
#define _TURN_H_

#include "zf_common_headfile.h"

//====================================================转向控制结构====================================================
typedef struct
{
    float target_angle;       // 目标转向角度（度）
    float current_angle;      // 当前偏航角（度）
    float angle_error;        // 角度误差（度）
    float last_angle_error;   // 上一次角度误差（度）
    float angle_error_sum;    // 角度误差积分
    
    float kp;                 // 比例系数
    float ki;                 // 积分系数
    float kd;                 // 微分系数
    
    float turn_output;        // 转向输出
    float turn_output_max;    // 转向输出最大值
    
    bool turn_enable;         // 转向使能标志
    bool turn_complete;       // 转向完成标志
    float turn_threshold;     // 转向完成阈值（度）
    
} turn_control_struct;

//====================================================外部变量声明====================================================
extern turn_control_struct turn_control;

//====================================================函数声明====================================================
//-------------------------------------------------------------------------------------------------------------------
// 函数简介     转向控制初始化
// 返回参数     void
// 使用示例     turn_control_init();
// 备注信息     初始化转向控制参数
//-------------------------------------------------------------------------------------------------------------------
void turn_control_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     设置转向目标角度
// 参数说明     target_angle - 目标转向角度（度）
// 返回参数     void
// 使用示例     turn_set_target_angle(90.0f);
// 备注信息     设置转向目标角度并开始转向
//-------------------------------------------------------------------------------------------------------------------
void turn_set_target_angle(float target_angle);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     转向控制计算
// 返回参数     float - 转向输出值
// 使用示例     float output = turn_control_calculate();
// 备注信息     计算转向控制输出，调用此函数进行转向控制
//-------------------------------------------------------------------------------------------------------------------
float turn_control_calculate(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取转向状态
// 返回参数     bool - 转向完成返回true，否则返回false
// 使用示例     bool complete = turn_is_complete();
// 备注信息     检查转向是否完成
//-------------------------------------------------------------------------------------------------------------------
bool turn_is_complete(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     停止转向
// 返回参数     void
// 使用示例     turn_stop();
// 备注信息     停止转向控制
//-------------------------------------------------------------------------------------------------------------------
void turn_stop(void);

#endif
